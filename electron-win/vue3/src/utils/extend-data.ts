// 转换规则：
//  - 前 4 项之间：每段插入 1 个值（共计 6 个值）
//  - 第 4 项～第 10 项之间：每段插入 2 个值（共计 18 个值）
//  - 第 10 项～第 40 项之间：每段插入 5 个值（共计 181 个值）
//  - 第 40 项与第 41 项之间：插入 11 个值（共计 12 个值）
//  - 保留 1 位小数，匀速等差，总计 6+18+181+12= 217 个值
//
// ===================================== 适用于 bmi =====================================
// <原始横坐标>
// age0: ['2岁', '2.5岁', '3岁', '3.5岁', '4岁', '4.5岁', '5岁', '5.5岁', '6岁', '6.5岁', '7岁', '7.5岁','8岁','8.5岁', '9岁', '9.5岁', '10岁'],
