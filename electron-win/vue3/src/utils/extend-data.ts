// <新的横坐标>：0~18岁每个月都是一个刻度，算上0共计 18x12+1 = 217 个刻度
// ---------------------------
// <原始数组>共计 33 项
// [
//   14.3, 14, 13.7, 13.5, 13.4, 13.3, 13.2, 13.2, 13.1, 13.1, 13.1, 13.1, 13.2, 13.2, 13.3, 13.4,
//   13.6, 13.7, 13.9, 14.1, 14.3, 14.5, 14.7, 14.8, 15, 15.2, 15.4, 15.5, 15.7, 15.8, 16, 16.1, 16.3,
// ]
// 转换规则：
//  - 各项之间：每段插入 5 个值（共计 193 个值）
//  - 保留 1 位小数，匀速等差，总计 32x6+1= 193 个值
//
// =====================================================================================

/**
 * 匀速插值工具函数
 * @param {number} start 起始值
 * @param {number} end 结束值
 * @param {number} count 要插入的数量
 * @returns {number[]} 插值数组（保留1位小数）
 */
function interpolate(start: number, end: number, count: number) {
  const step = (end - start) / (count + 1)
  const res = []
  for (let i = 1; i <= count; i++) {
    res.push(Math.round((start + step * i) * 10) / 10)
  }
  return res
}

/**
 * 按你指定规则转换数组 → 输出 217 项
 * @param {number[]} arr 原始41项数组
 * @returns {number[]} 转换后217项数组
 */
// 身高体重用
function transformHWData(arr: number[]) {
  const result = []

  // 规则1：前4项之间 → 每段插1个
  for (let i = 0; i < 3; i++) {
    result.push(arr[i])
    result.push(...interpolate(arr[i]!, arr[i + 1]!, 1))
  }
  result.push(arr[3])

  // 规则2：第4项～第10项 → 每段插2个
  for (let i = 3; i < 9; i++) {
    result.push(...interpolate(arr[i]!, arr[i + 1]!, 2))
    result.push(arr[i + 1])
  }

  // 规则3：第10项～第40项 → 每段插5个
  for (let i = 9; i < 39; i++) {
    result.push(...interpolate(arr[i]!, arr[i + 1]!, 5))
    result.push(arr[i + 1])
  }

  // 规则4：第40项～第41项 → 插11个
  result.push(...interpolate(arr[39]!, arr[40]!, 11))
  result.push(arr[40])

  return result
}

/**
 * 按你指定规则转换数组 → 输出 193 项
 * @param {number[]} arr 原始33项数组
 * @returns {number[]} 转换后193项数组
 */
// bmi用
function transformBMIData(arr: number[]) {
  const result = []

  // 规则1：各项之间 → 每段插5个
  for (let i = 0; i < 32; i++) {
    result.push(arr[i])
    result.push(...interpolate(arr[i]!, arr[i + 1]!, 5))
  }
  result.push(arr[32])

  return result
}

export { transformHWData, transformBMIData }

