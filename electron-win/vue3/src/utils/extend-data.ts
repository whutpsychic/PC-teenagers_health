// age0: ['10.5岁', '11岁', '11.5岁', '12岁', '12.5岁','13岁','13.5岁', '14岁', '14.5岁', '15岁', '15.5岁', '16岁', '16.5岁', '17岁'],
// age0: ['18岁'],
// <新的横坐标>：0~18岁每个月都是一个刻度，算上0共计 18x12+1 = 217 个刻度
// ---------------------------
// <原始数组>共计 41 项
// [
//   47.1, 54.6, 60.3, 64, 67.9, 71.5, 74.4, 76.9, 79.5, 82.1, 86.4, 89.7, 93.4, 96.7, 100, 103.3,
//   106.4, 109.1, 111.7, 114.6, 117.4, 119.9, 122.3, 124.6, 126.7, 128.7, 130.7, 132.9, 135.3, 138.1,
